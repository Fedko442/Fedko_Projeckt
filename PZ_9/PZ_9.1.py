#Даны имена девочек.Определить: какие из этих имён встречаются в группах на всех вторых курсах;
#какие имена есть только в некоторых группах;
#какие имена не встречаются ни в одной из групп.

# списки имён по группам 2 курса
g1 = {"Аня", "Лена", "Оля"}          # группа 2-1 (пример)
g2 = {"Оля", "Лена", "Катя"}         # группа 2-2
g3 = {"Лена", "Оля", "Ира"}          # группа 2-3

groups = [g1, g2, g3]

# 1) Имена, которые встречаются во ВСЕХ группах (пересечение)
in_all = set.intersection(*groups)

# 2) Имена, которые есть хотя бы в одной группе (объединение)
in_any = set.union(*groups)

# 3) Имена, которые есть ТОЛЬКО в некоторых группах
# (то есть есть хотя бы в одной, но не во всех)
only_some = in_any - in_all

print("Во всех группах:", in_all)
print("Только в некоторых группах:", only_some)

# 4) Имена, которые НЕ встречаются ни в одной группе
# если дан общий список всех имён (например, из задания)
all_names = {"Аня", "Лена", "Оля", "Катя", "Ира", "Маша"}  # пример
in_none = all_names - in_any

print("Не встречаются ни в одной группе:", in_none)
